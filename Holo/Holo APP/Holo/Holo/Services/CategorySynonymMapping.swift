    // MARK: - 查询方法

    /// 根据同义词查找标准的二级分类名称
    /// - Parameters:
    ///   - synonym: 可能的同义词（如「早饭」）
    ///   - type: 交易类型（支出/收入）
    /// - Returns: 标准名称（如「早餐」），若未找到则返回 nil
    static func findStandardSubCategoryName(_ synonym: String, type: TransactionType) -> String? {
        let normalized = synonym.trimmingCharacters(in: .whitespaces).lowercased()
        let synonyms = type == .expense ? expenseSubCategorySynonyms : incomeSubCategorySynonyms

        // 遍历所有映射，查找匹配的同义词
        for (standardName, synonymList) in synonyms {
            // 精确匹配标准名称
            if standardName.lowercased() == normalized {
                return standardName
            }
            // 匹配同义词列表
            for syn in synonymList {
                if syn.lowercased() == normalized {
                    return standardName
                }
            }
        }

        return nil
    }

    /// 根据同义词查找标准的一级分类名称
    /// - Parameter synonym: 可能的同义词
    /// - Returns: 标准名称，若未找到则返回 nil
    static func findStandardPrimaryCategoryName(_ synonym: String) -> String? {
        let normalized = synonym.trimmingCharacters(in: .whitespaces).lowercased()

        for (standardName, synonyms) in primaryCategorySynonyms {
            if standardName.lowercased() == normalized {
                return standardName
            }
            for syn in synonyms {
                if syn.lowercased() == normalized {
                    return standardName
                }
            }
        }

        return nil
    }

    /// 获取所有标准二级分类名称（按类型）
    static func getAllStandardSubCategoryNames(type: TransactionType) -> Set<String> {
        let synonyms = type == .expense ? expenseSubCategorySynonyms : incomeSubCategorySynonyms
        return Set(synonyms.keys)
    }

    /// 获取所有标准一级分类名称
    static var allStandardPrimaryCategoryNames: Set<String> {
        Set(primaryCategorySynonyms.keys)
    }
